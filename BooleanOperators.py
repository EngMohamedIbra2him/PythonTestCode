# and 
# or 
# not

age=26
country ="egypt"
rank = 10


print (age == 20 and country == "egypt" and rank >0)
print (age == 20 and country == "egypt" or rank >0)
print (age == 20 or country == "egypt" and rank >0)
print (not age == 20 and country == "egypt" and rank >0)






