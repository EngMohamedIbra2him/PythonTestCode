#---------------------------------
# type()
# All data in python is Object 
#---------------------------------

print(type(10)) # => Integr Number
print(type(10)) # => Integr Number
print(type(-59)) # => Integr Number

print(type(1.2)) # => float >= Floating point number
print(type(1.2)) # => float >= Floating point number
print(type(-20.2)) # => float >= Floating point number

print(type("Hello Python"))  # => str > string 
print(type("123"))  # => str > string 
print(type("-10.3"))  # => str > string 

print(type([1,2,3,4,5,6,7]))  # => list  
print(type(["ali","mohamed","osman"]))  # => list  
print(type([1,1.2,-16]))  # => list  

print(type(("mohamed","ali")))  # => tuple 
print(type((2,4)))   # => tuple 

print(type({"one":1,"two":2}))  #>= dict > Dictionary
print(type({1:"one",1:"Two"}))  #>= dict > Dictionary

print(type(2==2))   # => bool > Boolean 
print(type(2==4))   # => bool > Boolean 
 
