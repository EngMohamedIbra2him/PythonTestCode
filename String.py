# ------------------------------------
# #Indexing ( Acess single item)
# slicing  ( Acess Multiple items)
# [start:end]
# [start:end:step]
#-------------------------------------

myString="I love python"

print(myString[3])
print(myString[-6])


print(myString[3:10])
print(myString[1:10])
print(myString[:10])
print(myString[1:])
print(myString[:])
print(myString[1:20:1])
print(myString[1:20:2])




